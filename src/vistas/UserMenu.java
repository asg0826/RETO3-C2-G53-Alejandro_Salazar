    private void listarEmpleados(){
        
        String filtroBusqueda = txtBuscar.getText();
        String queryConsulta;
        if(filtroBusqueda.isEmpty()){
        
           queryConsulta = "SELECT * FROM empleado";
        }else{
           queryConsulta = "SELECT * FROM empleado WHERE nombreEmp LIKE '%" + filtroBusqueda + "%' or apellidos LIKE '%" + filtroBusqueda + "%'" ;
        }
        try{
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(queryConsulta);
            
            Object[] empleados = new Object[6];
            contenidoTablaEmpleados = (DefaultTableModel)tblEmpleados.getModel();
            while(rs.next()){
                empleados[0] = rs.getInt("idEmp");
                empleados[1] = rs.getString("nombreEmp");
                empleados[2] = rs.getString("apellidos");
                empleados[3] = rs.getString("tipoDocumento");
                empleados[4] = rs.getString("documento");
                empleados[5] = rs.getString("correo");
                contenidoTablaEmpleados.addRow(empleados);
                System.out.println("id: " + empleados[0] + ",nombre: " + empleados[1] + ", apellidos: " + empleados[2] + ", documento: "
                +empleados[3]+ " " +empleados[4]+  ", email"  + empleados[5] );
                tblEmpleados.setModel(contenidoTablaEmpleados);
            }
        }
        catch(SQLException e){
            System.out.println("ERROR");
        }
        
    }

    public void borrarDatosTabla() {
            for (int i = 0; i < tblEmpleados.getRowCount(); i++) {
                //Eliminamos todos los registros de empleados que tiene la tabla
                contenidoTablaEmpleados.removeRow(i);
                i = i-1;
            }
        }

